static inline uint32_t beamer_p4_crc_reflect_8(uint32_t data)
{
	return beamer_table_reflect[data & 0xff];
}

static inline uint32_t beamer_p4_crc_reflect_32(uint32_t data)
{
	return (beamer_p4_crc_reflect_8(data >> 0) << 24) |
		(beamer_p4_crc_reflect_8(data >> 8) << 16) |
		(beamer_p4_crc_reflect_8(data >> 16) << 8) |
		(beamer_p4_crc_reflect_8(data >> 24) << 0);
}

uint32_t beamer_p4_crc32(const char *buf, size_t len)
{
	uint32_t remainder = 0xFFFFFFFF;
	uint32_t final_xor_value = 0xFFFFFFFF;
	unsigned int byte;
	
	for (byte = 0; byte < len; byte++)
	{
		int data = beamer_p4_crc_reflect_8(buf[byte]) ^ (remainder >> 24);
		remainder = beamer_table_crc32[data] ^ (remainder << 8);
	}
	return beamer_p4_crc_reflect_32(remainder) ^ final_xor_value;
}

